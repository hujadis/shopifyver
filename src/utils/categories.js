// Helper function to get category by ID
export const getCategoryById = (id) => {
  return CATEGORIES[id] || null;
};

// Helper function to get all parent categories
export const getParentCategories = () => {
  const parents = [...new Set(CATEGORIES.map(cat => cat.parent))];
  return parents.sort();
};

// Helper function to get sub categories by parent
export const getSubCategories = (parent) => {
  const subs = [...new Set(CATEGORIES.filter(cat => cat.parent === parent).map(cat => cat.sub))];
  return subs.sort();
};

// Helper function to get sub2 categories by parent and sub
export const getSub2Categories = (parent, sub) => {
  const sub2s = [...new Set(CATEGORIES.filter(cat => cat.parent === parent && cat.sub === sub).map(cat => cat.sub2))];
  return sub2s.sort();
};
